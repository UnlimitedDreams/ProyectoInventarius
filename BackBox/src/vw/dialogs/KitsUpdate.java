    public void muevaLosDatosFre(TablaModel x) {
        for (int i = 0; i < 6; i++) {
            for (int k = 0; k < x.getNrofreq(); k++) {
                jTable1.setValueAt(x.frecuencias[i][k], k, i);
            }
        }
    }

